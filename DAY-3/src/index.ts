// 문자열 string타입
let name1: string = 'Alice';
console.log(name1);

// 숫자 number타입
let age: number = 30;
console.log(age);

// 논리 값 - 참과 거짓
let isStudent: boolean = true;
console.log(isStudent);

// null - 값이 없음
let emptyValue: null = null;
console.log(emptyValue);

// any 타입 - 모든타입
let variable: any = 'Hello';
console.log(variable);

variable = 42;
console.log(variable);


variable = true;
console.log(variable); 